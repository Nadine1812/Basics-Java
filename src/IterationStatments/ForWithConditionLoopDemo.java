package IterationStatments;

public class ForWithConditionLoopDemo {

	public static void main(String[] args) {
		
		//exemple1
		
		for (int i = 0 ; i < 5 ; i++ ) 
			System.out.println("Counter : " + i) ;
		
		//exemple2
		
		for ( int i = 0 , j = 10 ; i < j ; i++ , j--)
			System.out.println("Counter : i =  " + i+  " et j = " +j); 
	}

}
